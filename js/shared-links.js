(function bindManagedLinks() {
    // Only run if there are data-link-key elements and the links array is defined
    if (typeof HU_LINKS === 'undefined') return;
    
    const linkMap = Object.fromEntries(HU_LINKS.map((item) => [item.key, item]));
    const managedLinks = document.querySelectorAll("a[data-link-key]");

    managedLinks.forEach((anchor) => {
        const key = anchor.dataset.linkKey;
        const item = linkMap[key];
        if (!item) {
            console.warn(`[HU_LINKS] Missing config for key: ${key}`);
            return;
        }

        anchor.href = item.href;
        if (item.newTab) {
            anchor.target = "_blank";
            anchor.rel = "noopener noreferrer";
        } else {
            anchor.removeAttribute("target");
            anchor.removeAttribute("rel");
        }
    });
})();

